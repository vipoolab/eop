// Helper to build a plan item
function item(
  mpNumber: number,
  itemNumber: string,
  name: string,
  description: string,
  parentItem: string | null = null,
  order: number = 0,
  meta?: PlanItem["meta"]
): PlanItem {
  const docId = `seed-mp-${String(mpNumber).padStart(2, "0")}`;
  return {
    id: `${docId}-item-${itemNumber.replace(/\./g, "-")}`,
    documentId: docId,
    parentItemId: parentItem ? `${docId}-item-${parentItem.replace(/\./g, "-")}` : null,
    number: itemNumber,
    name,
    description,
    order,
    meta,
  };
}

