// Helper for items
function apItem(
  code: string,
  itemNumber: string,
  name: string,
  description: string,
  parent: string | null = null,
  order: number = 0,
  meta?: PlanItem["meta"]
): PlanItem {
  const docId = `seed-ap-${code}`;
  return {
    id: `${docId}-${itemNumber.replace(/\./g, "-")}`,
    documentId: docId,
    parentItemId: parent ? `${docId}-${parent.replace(/\./g, "-")}` : null,
    number: itemNumber,
    name,
    description,
    order,
    meta,
  };
}

