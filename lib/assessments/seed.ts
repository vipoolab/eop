import type { Assessment, AssessmentSubmission } from "./types";

/** Days offset from today (negative = past, positive = future) */
function d(offsetDays: number): string {
  const dt = new Date();
  dt.setDate(dt.getDate() + offsetDays);
  return dt.toISOString();
}

function makeSub(
  id: string,
  unitId: string,
  unitName: string,
  submittedBy: string,
  submittedByName: string,
  submittedByTitle: string,
  fileName: string,
  notes: string,
  daysAgo: number,
  status: AssessmentSubmission["status"] = "SUBMITTED",
  reviewNote?: string,
  reviewedByName?: string
): AssessmentSubmission {
  return {
    id,
    unitId,
    unitName,
    submittedAt: d(-daysAgo),
    submittedBy,
    submittedByName,
    submittedByTitle,
    fileName,
    notes,
    status,
    reviewNote,
    reviewedAt: reviewNote ? d(-Math.max(0, daysAgo - 2)) : undefined,
    reviewedByName,
  };
}

